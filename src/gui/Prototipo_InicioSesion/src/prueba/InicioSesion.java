    // Para que las imagenes no pierdan calidad
    private void SetImageLabel(JLabel labelName, String root) {
        ImageIcon image = new ImageIcon(root);
        Icon icon =
                new ImageIcon(
                        image.getImage()
                                .getScaledInstance(
                                        labelName.getWidth(),
                                        labelName.getHeight(),
                                        Image.SCALE_DEFAULT));
        labelName.setIcon(icon);
        this.repaint();
    }

